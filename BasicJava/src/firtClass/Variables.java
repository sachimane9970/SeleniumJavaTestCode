package firtClass;

public class Variables {

	public static void main(String[] args) {
		// int is premitive data type and data is variable and 50 is variable value
      
		int data=50; //integer local type variable
		System.out.println(data); //50
		
		int x =100; // integer local type variable
		System.out.println(x); //100
		
	    x=200; //local variable
	    System.out.println(x);//200
	    
	    char c ='g'; // char type local variable and value should written inside single quote 
	    System.out.println(c);
	    
	    boolean b= true;
	    System.out.println(b); //true
	    System.out.println(3>12); //false
	    
	    int e=100;
	    int d=200;
	    
	    boolean f= e>d; // boolean type local variable
	    System.out.println(f); //false
	}

}
